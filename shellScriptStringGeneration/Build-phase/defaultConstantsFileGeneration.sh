echo "-------------------- defaultConstantsFileGeneration.sh started --------------------"

#configure input variables for this script here
projectDir="$SRCROOT/shellScriptStringGeneration"
constantsDir="$projectDir/Constants"

# clean up current file
mkdir -p $constantsDir
rm $constantsDir/Constants.swift 2>/dev/null
touch $constantsDir/Constants.swift

# fill Constants.swift file with code
constantsFile="$constantsDir/Constants.swift"

echo "// This file is autogenerated, do not edit here" >> $constantsFile
echo "" >> $constantsFile
echo "struct Constants {" >> $constantsFile
echo "}" >> $constantsFile


echo "-------------------- defaultConstantsFileGeneration.sh stopped --------------------"
